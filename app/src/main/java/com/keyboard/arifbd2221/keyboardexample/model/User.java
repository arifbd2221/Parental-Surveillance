package com.keyboard.arifbd2221.keyboardexample.model;

public class User {
    public String fullname;
    public String education;
    public String email;
    public String phone;

    public User() {
    }

    public User(String fullname, String education, String email, String phone) {
        this.fullname = fullname;
        this.education = education;
        this.email = email;
        this.phone = phone;
    }
}
